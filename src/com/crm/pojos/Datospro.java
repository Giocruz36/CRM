package com.crm.pojos;

public class Datospro {
	private int id;
	private String dni_nie;
	private String nomemp;
	private String activemp;
	private float antig;
	private String puesto;
	private String diremp;
	private int tel;
	private String fax;
	private String correoe;
	private String comentarios;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public String getDni_nie() {
		return dni_nie;
	}

	public void setDni_nie(String dni_nie) {
		this.dni_nie = dni_nie;
	}

	public String getNomemp() {
		return nomemp;
	}

	public void setNomemp(String nomemp) {
		this.nomemp = nomemp;
	}

	public String getActivemp() {
		return activemp;
	}

	public void setActivemp(String activemp) {
		this.activemp = activemp;
	}

	public float getAntig() {
		return antig;
	}

	public void setAntig(float antig) {
		this.antig = antig;
	}

	public String getPuesto() {
		return puesto;
	}

	public void setPuesto(String puesto) {
		this.puesto = puesto;
	}

	public String getDiremp() {
		return diremp;
	}

	public void setDiremp(String diremp) {
		this.diremp = diremp;
	}

	public int getTel() {
		return tel;
	}

	public void setTel(int tel) {
		this.tel = tel;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public String getCorreoe() {
		return correoe;
	}

	public void setCorreoe(String correoe) {
		this.correoe = correoe;
	}

	public String getComentarios() {
		return comentarios;
	}

	public void setComentarios(String comentarios) {
		this.comentarios = comentarios;
	}

}
